# 当我们在传入函数时，有些时候，不需要显式地定义函数，直接传入匿名函数更方便
# >>> list(map(lambda x: x * x, [1, 2, 3, 4, 5, 6, 7, 8, 9]))
# [1, 4, 9, 16, 25, 36, 49, 64, 81]
# 关键字lambda表示匿名函数，冒号前面的x表示函数参数。

